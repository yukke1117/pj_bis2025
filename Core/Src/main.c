  /* 2) LCD_DISP (PC10) で LCD をリセット解除 */
  HAL_GPIO_WritePin(GPIOC, GPIO_PIN_10, GPIO_PIN_RESET);   // Low: Reset
  HAL_Delay(1);
  HAL_GPIO_WritePin(GPIOC, GPIO_PIN_10, GPIO_PIN_SET);     // High: Run

